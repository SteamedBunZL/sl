package com.tuixin11sms.tx.task;

public class TaskFinish {
	private static boolean isLoadFinished = true;
	private static boolean isDownFinished = true;
	private TaskFinish(){
		
	}
	public static boolean isLoadFinished() {
		return isLoadFinished;
	}
	public static void setLoadFinished(boolean isLoadFinished) {
		TaskFinish.isLoadFinished = isLoadFinished;
	}
	public static boolean isDownFinished() {
		return isDownFinished;
	}
	public static void setDownFinished(boolean isDownFinished) {
		TaskFinish.isDownFinished = isDownFinished;
	}
	
	
}
